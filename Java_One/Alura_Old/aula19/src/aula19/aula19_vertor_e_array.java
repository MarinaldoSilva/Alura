package aula19;



public class aula19_vertor_e_array {
	public static void main(String []args){
		int i =0;
		
		
		System.out.println("Incremento");
		
		for(i =0; i<5;i++) {
			System.out.println("I tem valor de:" +  i);
		}
		
		System.out.println("Decremento");
		
		for(i = 5; i>=0;i--) {
			System.out.println("Valor de 'i' = " + i );
		}
	}
}
